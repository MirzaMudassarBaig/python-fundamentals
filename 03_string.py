#Type casting, also known as type conversion, is the process of converting a value of one data type to another data type. In Python, type casting is used to convert a value of one type to another type, such as converting an integer to a string or a float to an integer.
x = 10
y = str(x)  # Convert integer to string
print(y)  # Output: "10"

x = "10"
y = int(x)  # Convert string to integer
print(y)  # Output: 10


x = 10.5
y = int(x)  # Convert float to integer
print(y)  # Output: 10


x = [1, 2, 3]
y = tuple(x)  # Convert list to tuple
print(y)  # Output: (1, 2, 3)


x = (1, 2, 3)
y = list(x)  # Convert tuple to list
print(y)  # Output: [1, 2, 3]
